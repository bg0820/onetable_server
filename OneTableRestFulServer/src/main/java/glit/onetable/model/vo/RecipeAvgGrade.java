package glit.onetable.model.vo;

public class RecipeAvgGrade {
	public int recipeIdx;
	public String recipeName;
	public int userIdx;
	public String id;
	public double price;
	public String recipeImg;
	public String nickname;
	public double avgGradePoint;


	public int getRecipeIdx() {
		return recipeIdx;
	}
	public void setRecipeIdx(int recipeIdx) {
		this.recipeIdx = recipeIdx;
	}
	public String getRecipeName() {
		return recipeName;
	}
	public void setRecipeName(String recipeName) {
		this.recipeName = recipeName;
	}
	public int getUserIdx() {
		return userIdx;
	}
	public void setUserIdx(int userIdx) {
		this.userIdx = userIdx;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public String getRecipeImg() {
		return recipeImg;
	}
	public void setRecipeImg(String recipeImg) {
		this.recipeImg = recipeImg;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public double getAvgGradePoint() {
		return avgGradePoint;
	}
	public void setAvgGradePoint(double avgGradePoint) {
		this.avgGradePoint = avgGradePoint;
	}



}
