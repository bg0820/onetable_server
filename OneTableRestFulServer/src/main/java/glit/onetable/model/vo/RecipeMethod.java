package glit.onetable.model.vo;

public class RecipeMethod {
	private int recipeMethodIdx;
	private int recipeIdx;
	private int num;
	private String img;
	private String content;

	public int getRecipeMethodIdx() {
		return recipeMethodIdx;
	}
	public void setRecipeMethodIdx(int recipeMethodIdx) {
		this.recipeMethodIdx = recipeMethodIdx;
	}
	public int getRecipeIdx() {
		return recipeIdx;
	}
	public void setRecipeIdx(int recipeIdx) {
		this.recipeIdx = recipeIdx;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}

}
